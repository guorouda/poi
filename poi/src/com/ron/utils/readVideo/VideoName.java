package com.ron.utils.readVideo;


public class VideoName {

	private int id;
	private String name;
	private String time;
	private String size;
	private String leaf;
	private String url;
	private String brief;
	private int VIDEO_DETAIL_ID;

	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public String getLeaf() {
		return leaf;
	}
	public void setLeaf(String leaf) {
		this.leaf = leaf;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getBrief() {
		return brief;
	}
	public void setBrief(String brief) {
		this.brief = brief;
	}
	public int getVIDEO_DETAIL_ID() {
		return VIDEO_DETAIL_ID;
	}
	public void setVIDEO_DETAIL_ID(int vIDEO_DETAIL_ID) {
		VIDEO_DETAIL_ID = vIDEO_DETAIL_ID;
	}
	
	
}
